function sumOfSquareAreas(a, b) {
    return (a * a) + (b * b);
}

console.log(sumOfSquareAreas(3, 4));
console.log(sumOfSquareAreas(5, 6));
console.log(sumOfSquareAreas(7, 8));

function gradeCalculator(homeworkAvg, quizAvg, examAvg) {
    return (homeworkAvg * 0.1) + (quizAvg * 0.15) + (examAvg * 0.75);
}

console.log(gradeCalculator(90, 80, 70));
console.log(gradeCalculator(85, 75, 65));
console.log(gradeCalculator(80, 70, 60));